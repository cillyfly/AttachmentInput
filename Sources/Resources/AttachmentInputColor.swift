import Foundation
import UIKit

class AttachmentInputColor {
    @nonobjc class var borderGray: UIColor {
        return UIColor(white: 140.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var primaryColor: UIColor {
        return UIColor(red: 216.0 / 255.0, green: 37.0 / 255.0, blue: 9.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var white: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
}
